export default class Calculadora{

    static soma(a,b){
        return a+b
    }

    static sub(a,b){
        return a-b
    }

    static div(a,b){
        return a/b
    }

    static mult(a,b){
        return a*b
    }

    static potencia(a,b){
        return Math.pow(a,b)        
    }
}